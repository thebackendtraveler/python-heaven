str_list = ["dog","bird","moose","salmon","cat","donkey"]
print("Initial list:", str_list)

# To sort the items in a list, use the sort() method of the list
str_list.sort()
print("Sorted list:",str_list)

# To reverse the items in a list (first becomes last), use the reverse() method of the list
str_list.reverse()
print("Reversed list:", str_list)